public class Q1_For {
    public static void main(String[] args) {
        int n =5;

        // print 0 to  4
        for(int i=0; i<n; i++){
            System.out.print(i);  // 0,1,2,3,4
        }

        // print 1 to 5
        for(int i=1; i<=n; i++){
            System.out.print(i);  // 1,2,3,4,5
        }

        // print Even
        for(int i=1; i<=n; i+=2){  // 1,3,5
            System.out.print(i);
        }

        // Reverse
        for(int i = n; i >= 1; i--){
            System.out.print(i);    // 4,3,2,1
        }

        // For-each
        

        




    }

}
